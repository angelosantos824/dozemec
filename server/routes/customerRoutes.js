const express = require("express");
const customer = require("../controllers/customerController");
const contacts = require("../controllers/customerContactController");
const addresses = require("../controllers/customerAddressController");
const documents = require("../controllers/customerDocumentController");
const consents = require("../controllers/customerConsentController");
const preferences = require("../controllers/customerPreferenceController");
const notes = require("../controllers/customerNoteController");
const relationships = require("../controllers/customerRelationshipController");
const history = require("../controllers/customerHistoryController");
const authenticate = require("../middlewares/authenticate");
const authorize = require("../middlewares/authorize");

const router = express.Router();

router.get("/", authenticate, authorize("customers.read"), customer.list);
router.post("/", authenticate, authorize("customers.create"), customer.create);
router.get("/:id/summary", authenticate, authorize("customers.read"), history.summary);
router.get("/:id/history", authenticate, authorize("customers.view_history"), history.list);
router.get("/:id/contacts", authenticate, authorize("customer_contacts.read"), contacts.list);
router.post("/:id/contacts", authenticate, authorize("customer_contacts.create"), contacts.create);
router.put("/:id/contacts/:contactId", authenticate, authorize("customer_contacts.update"), contacts.update);
router.delete("/:id/contacts/:contactId", authenticate, authorize("customer_contacts.delete"), contacts.remove);
router.get("/:id/addresses", authenticate, authorize("customer_addresses.read"), addresses.list);
router.post("/:id/addresses", authenticate, authorize("customer_addresses.create"), addresses.create);
router.put("/:id/addresses/:addressId", authenticate, authorize("customer_addresses.update"), addresses.update);
router.delete("/:id/addresses/:addressId", authenticate, authorize("customer_addresses.delete"), addresses.remove);
router.get("/:id/documents", authenticate, authorize("customers.view_documents"), documents.list);
router.post("/:id/documents", authenticate, authorize("customers.manage_documents"), documents.create);
router.put("/:id/documents/:documentId", authenticate, authorize("customers.manage_documents"), documents.update);
router.delete("/:id/documents/:documentId", authenticate, authorize("customers.manage_documents"), documents.remove);
router.get("/:id/consents", authenticate, authorize("customer_consents.read"), consents.list);
router.put("/:id/consents/:consentType", authenticate, authorize("customer_consents.update"), consents.update);
router.get("/:id/preferences", authenticate, authorize("customers.read"), preferences.show);
router.put("/:id/preferences", authenticate, authorize("customers.update"), preferences.update);
router.get("/:id/notes", authenticate, authorize("customer_notes.read"), notes.list);
router.post("/:id/notes", authenticate, authorize("customer_notes.create"), notes.create);
router.put("/:id/notes/:noteId", authenticate, authorize("customer_notes.update"), notes.update);
router.delete("/:id/notes/:noteId", authenticate, authorize("customer_notes.delete"), notes.remove);
router.get("/:id/relationships", authenticate, authorize("customer_relationships.read"), relationships.list);
router.post("/:id/relationships", authenticate, authorize("customer_relationships.create"), relationships.create);
router.delete("/:id/relationships/:relationshipId", authenticate, authorize("customer_relationships.delete"), relationships.remove);
router.patch("/:id/activate", authenticate, authorize("customers.activate"), customer.activate);
router.patch("/:id/deactivate", authenticate, authorize("customers.deactivate"), customer.deactivate);
router.patch("/:id/block", authenticate, authorize("customers.block"), customer.block);
router.patch("/:id/unblock", authenticate, authorize("customers.unblock"), customer.unblock);
router.get("/:id", authenticate, authorize("customers.read"), customer.show);
router.put("/:id", authenticate, authorize("customers.update"), customer.update);
router.delete("/:id", authenticate, authorize("customers.delete"), customer.remove);

module.exports = router;
